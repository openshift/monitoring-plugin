  echo "  CYPRESS_MOCK_NEW_METRICS=${CYPRESS_MOCK_NEW_METRICS:-$mock_new_metrics}"
  echo "  CYPRESS_SESSION=${CYPRESS_SESSION:-$session}"
  echo "  CYPRESS_DEBUG=${CYPRESS_DEBUG:-$debug}"
